const Order = require("../models/orderModel");
const Product = require("../models/productModel");
const ErrorHander = require("../utils/errorhander");
const catchAsyncErrors = require("../middleware/catchAsyncErrors");
const sendEmail = require("../utils/sendEmail");
const helper = require("../utils/helper")

// Create new Order
exports.newOrder = catchAsyncErrors(async (req, res, next) => {
  const {
    shippingInfo,
    orderItems,
    paymentInfo,
    itemsPrice,
    taxPrice,
    shippingPrice,
    totalPrice,
  } = req.body;

  const order = await Order.create({
    shippingInfo,
    orderItems,
    paymentInfo,
    itemsPrice,
    taxPrice,
    shippingPrice,
    totalPrice,
    paidAt: Date.now(),
    user: req.user._id,
  });

  let table_row_data=helper.tableRowGenerator(order.orderItems)
  const bodyMsg = {
    heading:"Order placed successfully.",
    message:`<p><h3>Your order details:</h3> <h4>Order Number:${order._id}</h4><table border="1" cellpadding="0" cellspacing="0" width="400px" align="center"><tr ><th>Product Name</th><th>Product</th><th>Price</th></tr>${table_row_data}</table><h3>Click here to view order details ${req.protocol}://${req.get('host')}/orders</h3></p>`
  }

  var message =await helper.parseTemplate(process.cwd()+"/utils/index.html",bodyMsg)

  await sendEmail({
    email: req.user.email,
    subject: `E-Commerce order details`,
    message,
  });

  res.status(201).json({
    success: true,
    order,
  });
});

// get Single Order
exports.getSingleOrder = catchAsyncErrors(async (req, res, next) => {
  const order = await Order.findById(req.params.id).populate(
    "user",
    "name email"
  );

  if (!order) {
    return next(new ErrorHander("Order not found with this Id", 404));
  }

  res.status(200).json({
    success: true,
    order,
  });
});

